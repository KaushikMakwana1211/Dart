void main() {
  int a = 2;
  int b = 3;
  double c = 2;

  // Add and assign
  print(a += b);

  // Subtract and assign
  print(a -= b);

  // Multiply and assign
  print(a *= b);
  
  // Divide and assign
  print(c /= b);

  // Integer divide and assign
  print(a ~/= b);

  // Modulus and assign
  print(a %= b);
}
