void main() {
  int a = 2;
  int b = 3;

  // Greater than
  var c = a > b;
  print(c);

  // Less than
  var d = a < b;
  print(d);

  // Greater than or equal to
  var e = a >= b;
  print(e);

  // Less than equal to
  var f = a <= b;
  print(f);
  
  // Equal to
  var g = a == b;
  print(g);

  // Not Equal to
  var h = a != b;
  print(h);
}
