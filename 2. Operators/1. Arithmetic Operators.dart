void main() {
  int a = 2;
  int b = 3;

  // Addition
  var c = a + b;
  print(c);

  // Subtraction
  var d = a - b;
  print(d);

  // Negation 
  var e = -d;
  print(e);

  // Multiply
  var f = a * b;
  print(f);

  // Division
  var g = a / b;
  print(g);

  // Division
  var h = a ~/ b;
  print(h);

  // Modulus
  var i = a % b;
  print(i);
}
